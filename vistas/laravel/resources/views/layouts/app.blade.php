            Swal.fire({
                title: message,
                text: 'Esta accion es irreversible!',
                icon: 'warning',
                showCancelButton: true,
                confirmButtonColor: '#3085d6',
                cancelButtonColor: '#d33',
                confirmButtonText: 'Eliminalo!'
